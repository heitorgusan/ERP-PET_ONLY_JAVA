package Domain.CustomerAnimals;

public class ProdServ {
	private String name;
	private String group;
	private double preco;
	private ProdutoServico PouS;
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getGroup() {
		return group;
	}
	public void setGroup(String group) {
		this.group = group;
	}
	public double getPreco() {
		return preco;
	}
	public void setPreco(double preco) {
		this.preco = preco;
	}
	public ProdutoServico getPouS() {
		return PouS;
	}
	public void setPouS(ProdutoServico pouS) {
		PouS = pouS;
	}
	
	
	
}
